inp = int(input ("Введите номер месяца (1-12)\n"))

#Вариант решения через list
seasons = [["зима", 12, 1, 2],["весна", 3, 4, 5],["лето", 6, 7, 8],["осень", 9, 10, 11]]
for elt in seasons:
    if inp in elt:
        print ("Время года -  " + elt [0])

#Вариант решения через dict
seasons = {"зима":[12, 1, 2], "весна":[3, 4, 5], "лето":[6, 7, 8], "осень":[9, 10, 11]}
for k, v in seasons.items ():
    if inp in v:
       print ("Время года -  " + k) 